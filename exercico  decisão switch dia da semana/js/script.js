


// Crie um programa qaue receba de 1 a 7 e mostre o dia da semana
// 1 - Domingo
// 2 - Segunda
// 3 - Terça
// 4 - Quarta
// 5 - Quinta
// 6 - Sexta
// 7 - Sábado


let dia = 7;


switch (dia) {
    case 1:
        console.log("Domingo");
        break;

    case 2:
        console.log("Segunda");
        break;
    case 3:
        console.log("Terça");
        break;

    case 4:
        console.log("Quarta");
        break;

    case 5:
        console.log("Quinta");
        break;

    case 6:
        console.log("Sexta");
        break;

    case 7:
        console.log("Sábado");
        break;
    default:
        console.log("Dia inválido!");

}