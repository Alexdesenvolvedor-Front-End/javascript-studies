


// Peça para o usuário digitar uma senha, se a senha for 1234 "Acesso permitido", caso contrário, "Senha incorreta"




let senha123 = prompt("Digite uma senha: ");
senha123 = parseInt(senha123);

if (senha123 == 123) {
    console.log("Acesso permitido!");
} else if (senha123 != 123) {
    console.log("Senha incorreta!");
}





