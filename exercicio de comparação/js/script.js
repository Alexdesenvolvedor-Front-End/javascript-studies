


// Pedir o usuário para digitar uma nota
let n1 = prompt("Digite uma nota: ");



//  Converter em número float
n1 = parseFloat(n1);



// Pedir o usuário para digitar mas uma nota
let n2 = prompt("Digite mas uma nota: ")



//  Converter em número float
n2 = parseFloat(n2);




// Mostrar na tela um boolean indicando se a média for maior que 5

media = (n1 + n2) / 2;

console.log(`${media > 5}`);


