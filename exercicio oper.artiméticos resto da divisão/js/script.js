

// Pedir para o usuário digitar um número
let n1 = prompt("Digite um número: ");


// Converter em número inteiro
n1 = parseInt(n1)


// Pedir para o usuário digitar outro número
let n2 = prompt("Digite outro número: ");


// Converter em número inteiro
n2 = parseInt(n2);


// Mostrar na tela o seguinte texto, o resto da divisão de (n1) poe (n2) é: (resultado);
resultado = n1 % n2;

console.log(`O resultado dessa operação é: ${resultado}`);