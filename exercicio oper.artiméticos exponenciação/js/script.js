



// Pedir para o usuário digitar um número
let n1 = prompt("Digite um número: ");


// Converter em um numero inteiro
n1 = parseInt(n1);


// Pedir para o usuário digitar outro número
let n2 = prompt("Digite outro número: ");



// Converter e um número inteiro
n2 = parseInt(n2);



// Mostrar o seguinte texto a exponenciação de (n1) por (n2) é: (resultado);
resultado = n1 ** n2;

console.log(`o resultado da exponenciação é: ${resultado}`);